export const getCollectionProducts = `#graphql
  query getCollectionProducts($handle: String!, $first: Int = 12) {
    collection(handle: $handle) {
      title
      products(first: $first) {
        nodes {
          id
          handle
          title
          featuredImage {
            url
            altText
            width
            height
          }
          priceRange {
            minVariantPrice {
              amount
              currencyCode
            }
          }
          compareAtPriceRange {
            minVariantPrice {
              amount
              currencyCode
            }
          }
        }
      }
    }
  }
` as const;

